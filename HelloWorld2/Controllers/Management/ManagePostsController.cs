            if (file != null)
            {
                string pic = System.IO.Path.GetFileName(file.FileName);
                string path = System.IO.Path.Combine(
                                       Server.MapPath("~/Content/images/post_images"), pic);
                // file is uploaded
                file.SaveAs(path);

